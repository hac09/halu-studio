// ブラウザの言語を検出
function detectLanguage() {
    const browserLang = navigator.language || navigator.userLanguage;
    
    if (browserLang.startsWith('ja')) return 'ja';
    if (browserLang.startsWith('ko')) return 'ko';
    if (browserLang.startsWith('zh-Hans') || browserLang === 'zh-CN') return 'zh-Hans';
    if (browserLang.startsWith('zh-Hant') || browserLang === 'zh-TW' || browserLang === 'zh-HK') return 'zh-Hant';
    if (browserLang.startsWith('zh')) return 'zh-Hans'; // デフォルトは簡体字
    
    return 'en'; // デフォルトは英語
}

// ページの言語を設定
function setLanguage(lang) {
    const elements = document.querySelectorAll('[data-i18n]');
    elements.forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (translations[lang] && translations[lang][key]) {
            // テキストノードのみを置き換え（子要素を保持）
            if (element.childNodes.length === 1 && element.childNodes[0].nodeType === Node.TEXT_NODE) {
                element.textContent = translations[lang][key];
            } else if (element.childNodes.length === 0) {
                element.textContent = translations[lang][key];
            } else {
                // 子要素がある場合は最初のテキストノードのみを置き換え
                for (let i = 0; i < element.childNodes.length; i++) {
                    if (element.childNodes[i].nodeType === Node.TEXT_NODE) {
                        element.childNodes[i].textContent = translations[lang][key];
                        break;
                    }
                }
            }
        }
    });
    
    // HTML lang属性を更新
    document.documentElement.lang = lang;
}

// ページ読み込み時に言語を設定
document.addEventListener('DOMContentLoaded', () => {
    const lang = detectLanguage();
    setLanguage(lang);
});

